public class Triangle {
    private double bokA;
    private double bokB;
    private double bokC;

    public Triangle(int bokA, int bokB, int bokC) {
        this.bokA = bokA;
        this.bokB = bokB;
        this.bokC = bokC;
    }

    public double getBokA() {
        return bokA;
    }

    public void setBokA(int bokA) {
        this.bokA = bokA;
    }

    public double getBokB() {
        return bokB;
    }

    public void setBokB(int bokB) {
        this.bokB = bokB;
    }

    public double getBokC() {
        return bokC;
    }

    public void setBokC(int bokC) {
        this.bokC = bokC;
    }
}
